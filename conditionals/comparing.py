# Python can be used to compare values to each other.
# We use Operators to perform comparisons, and can compare Equations or Strings. We can look to see if something is bigger than, smaller than or equal to another value
# A comparison operation results in a boolean output of True or False
# Other than equality, you can't do a comparison between different data types, like a string and int

print("cat" == "cat")
print(4 > 2)
print(2**2 == 4)
# print (2 > "2") - results in an error, but the below is valid:
print("2" == 2)
